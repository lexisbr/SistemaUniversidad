    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id_curse = searchTxt.getText();
        Curso curse = Manejador.searchCurso(id_curse);
        if (curse != null)
        {
            cursoSeleccionado = curse;
            codigoTxt.setText(String.valueOf(curse.getId()));
            nombreTxt.setText(curse.getName());
            semestreTxt.setText(String.valueOf(curse.getSemester()));
            creditosTxt.setText(String.valueOf(curse.getCredits()));
            searchTxt.setText("");
            editarButton.setEnabled(true);
        } else
        {
            JOptionPane.showMessageDialog(this, "El curso no existe", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

