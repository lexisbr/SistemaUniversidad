    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        // TODO add your handling code here:
        int id = cursoSeleccionado.getId();
        String name = nombreTxt.getText();
        int semester  = Integer.parseInt(semestreTxt.getText());
        int credits = Integer.parseInt(creditosTxt.getText());
        Curso curse = new Curso(id, name, semester, credits);
        if (Manejador.updateCurso(curse))
        {
            JOptionPane.showMessageDialog(this, "Se ha actualizado correctamente el curso");
            editarButton.setEnabled(false);
            codigoTxt.setText("");
            nombreTxt.setText("");
            semestreTxt.setText("");
            creditosTxt.setText("");
            cursoSeleccionado = null;
        } else
        {
            JOptionPane.showMessageDialog(this, "No se ha podido actualizar correctamente", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarButtonActionPerformed

