    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            int id = Integer.parseInt(idTxt.getText());
            String name = nombreTxt.getText().trim();
            String type = tipoTxt.getSelectedItem().toString();
            String password = passwordTxt.getText();
            if (!name.equals("") && !type.equals("") && !password.equals(""))
            {
                if (Manejador.addUser(id, name, password, type))
                {
                    JOptionPane.showMessageDialog(this, "Se ha agregado al usuario: " + id + " correctamente. ");
                } else
                {
                    JOptionPane.showMessageDialog(this, "No se ha agregado al usuario: " + id + " porque este ID ya existe. ");
                }
            } else
            {
                JOptionPane.showMessageDialog(this, "Los campos estan incompletos");
            }

        } catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "El ID debe ser numerico");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

